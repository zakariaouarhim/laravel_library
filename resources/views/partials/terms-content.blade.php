    <li>يُعدّ طلبك مؤكداً فور استلامك بريد التأكيد الإلكتروني.</li>
    <li>نحتفظ بالحق في إلغاء أي طلب في حال عدم توفر المنتج أو وجود خطأ في السعر، مع إعادة المبلغ كاملاً.</li>
